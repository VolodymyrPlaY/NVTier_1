"""
Нагадаймо методи, що вже розглядалися, для видалення зайвих пробілів на початку і в кінці рядка.

Щоб видалити пропуски у правого краю рядка, використовують метод rstrip:

name = "Avril Lavigne        "
print(name.rstrip())  # Avril Lavigne
Щоб видалити пропуски біля лівого краю рядка, використовують метод lstrip:

name = "         Avril Lavigne"
print(name.lstrip())  # Avril Lavigne
Щоб видалити пропуски з обох боків рядка, використовують метод strip:

name = "         Avril Lavigne          "
print(name.strip())  # Avril Lavigne
Вони нам дуже стануть у пригоді при вирішенні наступного завдання
"""
'''
Ваша компанія проводить маркетингові кампанії за допомогою SMS-розсилок. Автоматичний збір телефонних номерів із бази даних формує певний перелік. Але клієнти залишають свої номери у довільному вигляді:

    "    +38(050)123-32-34",
    "     0503451234",
    "(050)8889900",
    "38050-111-22-22",
    "38050 111 22 11   ",
Сервіс розсилок чудово розуміє і може відправити SMS клієнту, тільки якщо телефонний номер містить правильні цифри. Необхідно реалізувати функцію sanitize_phone_number, яка прийматиме рядок з телефонним номером та буде нормалізувати його, тобто. буде прибирати символи (, -, ), + та пробіли.

Результат:

380501233234
0503451234
0508889900
380501112222
380501112211
'''
"""
def sanitize_phone_number(phone):
    # Вилучення всіх символів, крім цифр
    cleaned_phone = ''.join(char for char in phone if char.isdigit())
    
    # Перевірка, чи номер телефону має довжину 10 або 12 цифр
    if len(cleaned_phone) == 10:
        # Додаємо префікс для внутрішнього використання (можна змінити за потреби)
        normalized_phone = '38' + cleaned_phone
    elif len(cleaned_phone) == 12:
        # Вже має правильну довжину
        normalized_phone = cleaned_phone
    else:
        # Неправильна довжина номера телефону
        normalized_phone = None
    
    return normalized_phone

# Приклад використання
phone_numbers = [
    "    +38(050)123-32-34",
    "     0503451234",
    "(050)8889900",
    "38050-111-22-22",
    "38050 111 22 11   ",
]

for phone in phone_numbers:
    result = sanitize_phone_number(phone)
    print(result)
# Ця функція вилучає всі символи, крім цифр, та нормалізує номер телефону, додаючи префікс "38" для внутрішнього використання (можна змінити за потреби). Якщо номер телефону має неправильну довжину, функція поверне None.
"""

def sanitize_phone_number(phone):
    result = ""
    remove_chars = "(, -, ), +"
    for char in phone:
        if char not in remove_chars:
            result += char
    return result.strip()

phone1 = "(050) 8 88 9900      "
result = sanitize_phone_number(phone1)
print(result) 