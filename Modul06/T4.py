def add_employee_to_file(record, path):
    # Відкриваємо файл за вказаним шляхом для дозапису ('a')
    file = open(path, 'a')
    
    # Додаємо співробітника до файлу (з символом нового рядка)
    file.write(record + '\n')
    
    # Закриваємо файл
    file.close()

